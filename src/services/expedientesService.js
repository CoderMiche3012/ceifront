import API from "./api";
const BASE_URL = "/api/beneficiarios/expedientes";
import { formatError } from "../utils/errorHandlers";

//obtener periodos
export const obtenerExpediente = async () => {
  try {
    const res = await API.get(`${BASE_URL}/`);
    return res.data;
  } catch (error) {
    console.log(error)
    // Extraemos la data del error de Axios
    const errorData = error.response?.data || error;

    if (errorData) {
      throw new Error(formatError(errorData));
    }

    throw new Error(formatError(error.message));
  }
};
